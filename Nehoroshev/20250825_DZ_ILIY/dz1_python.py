# Машина проехала 47 километров за 20 минут. Какова скорость машины в километрах в час?
# Решение: 1. Вычисляем скорость:
# 47 * 60 / 20 = 141 км / ч
# Ответ: 141 км в час
#
def calculate_speed(distance_km, time_minutes):
  """
  Вычисляет скорость в километрах в час, зная пройденное расстояние в километрах и время в минутах.

  Args:
    distance_km: Пройденное расстояние в километрах.
    time_minutes: Время, затраченное на прохождение расстояния, в минутах.

  Returns:
    Скорость в километрах в час.
  """
  speed_km_per_hour = (distance_km * 60) / time_minutes
  return speed_km_per_hour

# Данные из условия задачи
distance = 47  #километров
time = 20      #минут

# Вызываем функцию для расчета скорости
speed = calculate_speed(distance, time)

# Выводим результат
print(f"Скорость машины: {speed} км/ч")